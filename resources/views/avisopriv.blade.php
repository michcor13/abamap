<h2 class="titulosaviso"> Recopilación de Otros datos </h2>
<h3 class="textosaviso">
Nosotros y nuestros proveedores de servicios podemos recopilar Otros datos de diversas 
formas, que incluyen:
    <br>
● A través de su explorador o dispositivo
<br>
● Determinados datos se recopilan a través de la mayoría de exploradores o automáticamente a 
través de su dispositivo, como su dirección de Media Access Control (MAC), tipo de ordenador 
(Windows o Mac), resolución de pantalla, nombre y versión del sistema operativo, fabricante 
y modelo del dispositivo, idioma, tipo y versión del explorador de Internet, así como el
 nombre y la versión de los Servicios (como la Aplicación) que utiliza.
 <br>
● A través de su uso de una Aplicación
<br>
● Al descargar y utilizar una Aplicación, nosotros y nuestros proveedores de servicios 
podemos realizar un seguimiento y recopilar datos de uso de la Aplicación, como la fecha y 
hora en la que la Aplicación de su dispositivo accede a nuestros servidores y qué datos y 
archivos se han descargado a la Aplicación según su número de dispositivo.
<br>
● Mediante cookies
<br>
● Las cookies son datos almacenados directamente en el ordenador que utiliza. Las cookies 
nos permiten recopilar datos como tipo de explorador, tiempo pasado en los Servicios, páginas 
visitadas, páginas web de referencia y otros datos de tráfico. Para obtener más información 
sobre las cookies, consulte nuestra Política de cookies.
<br>
Actualmente no respondemos a las señales de no seguimiento del explorador. Si no desea que se
 recopilen datos mediante cookies, la mayoría de exploradores le permiten rechazar 
 automáticamente las cookies o le dan la opción de rechazar o aceptar una cookie 
 (o cookies) concreta(s) de una página web en particular. 
 <br>
● Mediante etiquetas de píxeles y otras tecnologías similares
<br>
● Las etiquetas de píxeles (también denominadas balizas web y GIF transparentes) 
se pueden utilizar para, entre otras cosas, realizar un seguimiento de las acciones de 
los usuarios de los Servicios (incluidos destinatarios de correo electrónico), medir el 
rendimiento de nuestras campañas de marketing y compilar estadísticas sobre el uso de los 
Servicios y tasas de respuesta.
<br>
● Dirección IP
<br>
● Su proveedor de servicios de Internet asigna automáticamente su dirección IP a su ordenador. 
Una dirección IP se puede identificar y registrar automáticamente en nuestros archivos de 
registro del servidor siempre que un usuario accede a los Servicios, junto con la hora de 
la visita y las páginas visitadas. La recopilación de direcciones IP es una práctica estándar
 y la realizan automáticamente muchas páginas web, aplicaciones y otros servicios. Utilizamos
  direcciones IP para fines como el cálculo de niveles de uso, el diagnóstico de problemas
   del servidor y la administración de los Servicios. También podemos obtener su ubicación 
   aproximada de su dirección IP.
   <br>
● Ubicación física
<br>
● Podemos recopilar la ubicación física de su dispositivo mediante, por ejemplo, el uso de 
señales de satélites, torres de telefonía móvil o Wi-Fi. Podemos utilizar la ubicación 
física de su dispositivo para proporcionarle servicios y contenido personalizados basados
 en la ubicación. En algunos casos, se le puede permitir aceptar o denegar dichos usos de 
 la ubicación de su dispositivo, pero, si lo hace, es posible que no podamos proporcionarle 
 los servicios y contenido personalizados correspondientes.
